test("regexIDUser", () => {
  const strInput =
    "/ping <@123456789012345678> <@!123456789012345678> <123456789012345678> <1234567890123456789> <@1234567890123456789> <@!1234567890123456789>";

  const regex = /((<?@?!?)(?=[0-9]))|((?<=[0-9])>)/g;

  const strOutput =
    "/ping 123456789012345678 123456789012345678 123456789012345678 1234567890123456789 1234567890123456789 1234567890123456789";

  expect(strInput.replace(regex, "")).toEqual(strOutput);
});
